    private void btncalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncalcularActionPerformed
        if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo nome é Obrigatorio");
            return;
        }
        
        if(txtidade.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo idade é Obrigatorio");
            return;
        }
        
         if(txtpeso.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo peso é Obrigatorio");
            return;
        }
         if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo altura é Obrigatorio");
            return;
        }
        
        float altura = Float.valueOf(txtaltura.getText());
        float peso = Float.valueOf(txtpeso.getText());

        float calcular = (peso) / (altura * altura);
        lblresultado.setText(Float.toString(calcular));

        if (calcular < 18) {
            JOptionPane.showMessageDialog(null, "Seu IMC = " + calcular + "Voce esta abaixo do peso");
        } else if (calcular >= 18 && calcular <= 25) {
            JOptionPane.showMessageDialog(null, "Seu IMC = " + calcular + "Parabéns voce está no peso ideal!!!");

        } else if (calcular >= 26 && calcular <= 35) {
            JOptionPane.showMessageDialog(null, "Seu IMC = " + calcular + "Obesidade Grau 1");

        } else if (calcular >= 36 && calcular <= 39) {
            JOptionPane.showMessageDialog(null, "Seu IMC = " + calcular + "Obesidade Grau 2 (severa)");

        } else if (calcular >= 40) {
            JOptionPane.showMessageDialog(null, "Seu IMC = " + calcular + "Obesidade Grau 3 (mórbida");

        }
    }//GEN-LAST:event_btncalcularActionPerformed

