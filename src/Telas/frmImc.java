    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
    if(txtnome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo nome é Obrigatorio");
            return;
        }
        
        if(txtidade.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo idade é Obrigatorio");
            return;
        }
        
         if(txtpeso.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo peso é Obrigatorio");
            return;
        }
         if(txtaltura.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo altura é Obrigatorio");
            return;
        }
        
         if(lblresultado.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "O resultado é Obrigatorio");
            return;
         }
        try {
            Tbimc obj = new Tbimc();
            obj.setNome(txtnome.getText());
            obj.setIdade(Integer.parseInt(txtidade.getText()));
            obj.setPeso(Float.parseFloat(txtpeso.getText()));
            obj.setAltura(Float.parseFloat(txtaltura.getText()));
            obj.setResultado(Float.parseFloat(lblresultado.getText()));

            UsuariosDAO dao = new UsuariosDAO();
            dao.cadastrarUsuario(obj);

            JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar" + erro);
        }

    }//GEN-LAST:event_btnsalvarActionPerformed

